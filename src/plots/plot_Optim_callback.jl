]

function mystepped_x_and_y(times, values)
    t₀ = times[1]
    x = [t₀]
    oldv = values[2]
    y = [oldv]
    for (t, v) in zip(times[2:end], values[2:end])
        push!(x, t)
        push!(x, t)
        push!(y, oldv)
        push!(y, v)
        oldv = v
    end
    return x .- t₀, y
end

p = plot()
for (i, method) in enumerate(list_methods)
    x, y = mystepped_x_and_y(data[method][:, 1], data[method][:, 4])
    global p
    p = plot!(p,
        x, y,
        color=ColorBrewer.palette("Set1", 4)[i],
        label=method,
    )
end

